export const updateResearchStatus = internalMutation({
  args: {
    companyId: v.id("outreachCompanies"),
    status: v.union(
      v.literal("pending"),
      v.literal("researching"),
      v.literal("done"),
      v.literal("failed")
    ),
    summary: v.optional(v.string()),
  },
  handler: async (ctx, { companyId, status, summary }) => {
    const patch: Record<string, unknown> = { researchStatus: status, updatedAt: Date.now() };
    if (summary !== undefined) patch.researchSummary = summary;
    await ctx.db.patch(companyId, patch);
  },
});

export const enrichCompany = internalMutation({
  args: {
    companyId: v.id("outreachCompanies"),
    domain: v.optional(v.string()),
    logoUrl: v.optional(v.string()),
    linkedinUrl: v.optional(v.string()),
    websiteUrl: v.optional(v.string()),
    isYcBacked: v.optional(v.boolean()),
    careersUrl: v.optional(v.string()),
    description: v.optional(v.string()),
    employeeCount: v.optional(v.number()),
    industry: v.optional(v.string()),
    fundingStage: v.optional(v.string()),
  },
  handler: async (ctx, { companyId, ...fields }) => {
    const patch: Record<string, unknown> = { updatedAt: Date.now() };
    for (const [k, val] of Object.entries(fields)) {
      if (val !== undefined) patch[k] = val;
    }
    await ctx.db.patch(companyId, patch);
  },
});

export const finalizeResearch = internalMutation({
  args: { companyId: v.id("outreachCompanies") },
  handler: async (ctx, { companyId }) => {
    const company = await ctx.db.get(companyId);
    if (!company) return;

    const jobs = await ctx.db
      .query("outreachJobs")
      .withIndex("by_company", (q) => q.eq("companyId", companyId))
      .collect();

    const contacts = await ctx.db
      .query("outreachContacts")
      .withIndex("by_company", (q) => q.eq("companyId", companyId))
      .collect();

    const existingSteps = await ctx.db
      .query("outreachSteps")
      .withIndex("by_company", (q) => q.eq("companyId", companyId))
      .collect();

    const existingLabels = existingSteps.map((s) => s.label.toLowerCase());

    const hasLabel = (keyword: string) =>
      existingLabels.some((l) => l.includes(keyword.toLowerCase()));

    const now = Date.now();
    let order = existingSteps.length;

    const stepsToAdd: Array<{ label: string }> = [];

    const linkedinJobs = jobs.filter((j) => j.source === "linkedin");
    if (linkedinJobs.length > 0 && !hasLabel("linkedin")) {
      stepsToAdd.push({ label: "Apply on LinkedIn" });
    }

    if (company.careersUrl && !hasLabel("company portal")) {
      stepsToAdd.push({ label: "Apply on company portal" });
    }

    if (company.isYcBacked && !hasLabel("yc portal")) {
      stepsToAdd.push({ label: "Apply via YC portal" });
    }

    const contactsWithEmail = contacts.filter((c) => c.email);
    if (contactsWithEmail.length > 0 && !hasLabel("cold email")) {
      stepsToAdd.push({ label: "Send cold emails" });
    }

    const contactsWithLinkedin = contacts.filter((c) => c.linkedinUrl);
    if (contactsWithLinkedin.length > 0 && !hasLabel("linkedin dm")) {
      stepsToAdd.push({ label: "Send LinkedIn DMs" });
    }

    for (const step of stepsToAdd) {
      await ctx.db.insert("outreachSteps", {
        companyId,
        label: step.label,
        status: "pending",
        order: order++,
        isAutoGenerated: true,
        updatedAt: now,
      });
    }

    const jobCount = jobs.length;
    const contactCount = contacts.length;
    const summary = `Research complete: ${jobCount} job${jobCount !== 1 ? "s" : ""} found, ${contactCount} contact${contactCount !== 1 ? "s" : ""} found.`;

    await ctx.db.patch(companyId, {
      researchStatus: "done",
      researchSummary: summary,
      updatedAt: now,
    });
  },
});

