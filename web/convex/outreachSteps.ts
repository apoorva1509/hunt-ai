import { mutation, query, internalMutation } from "./_generated/server";
import { v } from "convex/values";
import { requirePerson } from "./helpers/auth";

const stepStatusValidator = v.union(
  v.literal("pending"),
  v.literal("done"),
  v.literal("skipped")
);

export const listByCompany = query({
  args: { companyId: v.id("outreachCompanies") },
  handler: async (ctx, { companyId }) => {
    const steps = await ctx.db
      .query("outreachSteps")
      .withIndex("by_company", (q) => q.eq("companyId", companyId))
      .collect();
    return steps.sort((a, b) => a.order - b.order);
  },
});

export const create = mutation({
  args: {
    companyId: v.id("outreachCompanies"),
    label: v.string(),
    status: v.optional(stepStatusValidator),
    order: v.optional(v.number()),
    isAutoGenerated: v.optional(v.boolean()),
  },
  handler: async (ctx, args) => {
    await requirePerson(ctx);
    let order = args.order;
    if (order === undefined) {
      const existing = await ctx.db
        .query("outreachSteps")
        .withIndex("by_company", (q) => q.eq("companyId", args.companyId))
        .collect();
      order = existing.length;
    }
    return await ctx.db.insert("outreachSteps", {
      companyId: args.companyId,
      label: args.label,
      status: args.status ?? "pending",
      order,
      isAutoGenerated: args.isAutoGenerated,
      updatedAt: Date.now(),
    });
  },
});

export const updateStatus = mutation({
  args: {
    id: v.id("outreachSteps"),
    status: stepStatusValidator,
  },
  handler: async (ctx, { id, status }) => {
    await requirePerson(ctx);
    await ctx.db.patch(id, { status, updatedAt: Date.now() });
  },
});

