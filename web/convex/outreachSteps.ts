export const listAllWithCompany = query({
  args: {},
  handler: async (ctx) => {
    const { getCurrentPerson } = await import("./helpers/auth");
    const person = await getCurrentPerson(ctx);
    if (!person) return [];
    const userId = person.clerkTokenIdentifier!;

    const companies = await ctx.db
      .query("outreachCompanies")
      .withIndex("by_user", (q) => q.eq("userId", userId))
      .collect();

    const results = [];
    for (const company of companies) {
      const steps = await ctx.db
        .query("outreachSteps")
        .withIndex("by_company", (q) => q.eq("companyId", company._id))
        .collect();
      for (const step of steps) {
        results.push({
          ...step,
          companyName: company.name,
          companyDomain: company.domain,
          companyLogoUrl: company.logoUrl,
          companyStatus: company.status,
          roleAppliedFor: company.roleAppliedFor,
        });
      }
    }

    return results.sort((a, b) => {
      // Pending first, then by updatedAt desc
      if (a.status === "pending" && b.status !== "pending") return -1;
      if (a.status !== "pending" && b.status === "pending") return 1;
      return b.updatedAt - a.updatedAt;
    });
  },
});
